import { DataTypes } from 'sequelize';
import sequelize from '../config/db.js';

const CustomerVerificationToken = sequelize.define('CustomerVerificationToken', {
  id: {
    type: DataTypes.INTEGER,
    primaryKey: true,
    autoIncrement: true,
    allowNull: false
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false
  },
  code: {
    type: DataTypes.STRING,
    allowNull: false
  },
  expiresAt: {
    type: DataTypes.DATE,
    allowNull: false
  },
  used: {
    type: DataTypes.BOOLEAN,
    defaultValue: false
  }
}, {
  tableName: 'CustomerVerificationTokens',
  timestamps: true
});

export default CustomerVerificationToken;

